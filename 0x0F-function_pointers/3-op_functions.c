#include "3-calc.h"

/**
* op_add - adds two ints
* @a: int
* @b: int
*
* Description: simple sum
* Return: an int
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtracts two ints
* @a: int
* @b: int
*
* Description: simple subtraction
* Return: an int
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiplies two ints
* @a: int
* @b: int
*
* Description: simple product
* Return: an int
*/

int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - divides two ints
* @a: int
* @b: int
*
* Description: simple division
* Return: an int
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - calculates the remainder of the division two ints
* @a: int
* @b: int
*
* Description: simple modulo
* Return: an int
*/

int op_mod(int a, int b)
{
	return (a % b);
}

